#include <stdio.h>

int main() {
  int a = 16;
  int b = 3;

  printf("16 + 3 = %d\n", a + b);
  printf("3 - 16 = %d\n", b - a);
  printf("16 * 3 = %d\n", a * b);
  printf("16 modulo 3 = %d\n", a % b);
  printf("16 == 3 = %d\n", a == b);
  printf("16 > 3 = %d\n", a > b);

  return 0;
}


